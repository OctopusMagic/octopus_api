def get_departamento_by_name(name: str) -> dict:
    return DEPARTAMENTOS.get(name.upper(), {})

def get_departamento_by_codigo(codigo: str) -> dict:
    for departamento in DEPARTAMENTOS.values():
        if departamento.get('codigo') == codigo:
            return departamento
    return {}

def get_municipio_by_name(name: str) -> str:
    for departamento in DEPARTAMENTOS.values():
        municipios = departamento.get('municipios', {})
        for municipio, codigo in municipios.items():
            if municipio == name.upper():
                return codigo
    return ''
